interface AsciiArtProps {
  art: string;
  className?: string;
}

export function AsciiArt({ art, className }: AsciiArtProps) {
  return (
    <pre
      className={`font-mono text-terminal-primary text-glow text-xs leading-tight whitespace-pre ${className || ""}`}
    >
      {art}
    </pre>
  );
}

export function TypewriterText({
  text,
  className,
}: {
  text: string;
  className?: string;
}) {
  return (
    <span className={`inline-block overflow-hidden whitespace-nowrap border-r-2 border-terminal-primary animate-pulse ${className || ""}`}>
      {text}
    </span>
  );
}
